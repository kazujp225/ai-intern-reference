create table if not exists public.internships (
  id text primary key,
  category text not null,
  company text not null,
  title text not null,
  band text,
  badge text,
  badge_new boolean default false,
  tags text[] default '{}',
  salary text,
  location text,
  workdays text,
  about text,
  skills text[] default '{}',
  welcome text[] default '{}',
  benefits text[] default '{}',
  created_at timestamptz not null default now()
);

create index if not exists internships_category_idx on public.internships (category);

-- RLS: 全員読める
alter table public.internships enable row level security;
drop policy if exists "internships_select_all" on public.internships;
create policy "internships_select_all" on public.internships for select using (true);

